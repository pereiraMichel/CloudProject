    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        if(radioNovo.isSelected()){
            principal = new telaPrincipal();
            novoCliente = new dClientes(principal, true);
            novoCliente.setTitle("NOVO CLIENTE");
            novoCliente.setLocationRelativeTo(novoCliente);
            novoCliente.setVisible(true);
        }else if(radioOpcoes.isSelected()){
            principal = new telaPrincipal();
            operacionalClientes = new dOpClientes(principal, true);
            operacionalClientes.setTitle("OPERAÇÕES - CLIENTES");
            operacionalClientes.setLocationRelativeTo(operacionalClientes);
            operacionalClientes.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Por favor, selecione a opção.");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btEntrarActionPerformed

